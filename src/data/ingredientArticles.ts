/**
 * Find an article that matches the given recommendation text.
 *
 * Algorithm: lowercase both sides, do a substring containment check
 * over each registered slug until a hit is found. Returns the
 * matched slug (which is also the lookup key in INGREDIENT_ARTICLES)
 * or null when no slug matches. Order of iteration matters — more
 * specific slugs MUST come before generic ones so that "коэнзим q10"
 * wins over a hypothetical future "q10" entry placed last.
 */
export function findArticleSlug(text: string): string | null {
  const lower = text.toLowerCase();
  for (const slug of Object.keys(INGREDIENT_ARTICLES)) {
    if (lower.includes(slug)) return slug;
  }
  return null;
}

/**
 * Lookup helper for the modal component — returns the typed article
 * object so the modal can render its 3 fields.
 */
export function findArticle(text: string): IngredientArticle | null {
  const slug = findArticleSlug(text);
  return slug ? INGREDIENT_ARTICLES[slug] : null;
}
