/**
 * 2026-07-01 — Clickable recommendations knowledge layer.
 *
 * User request: «сделай кликабельными рекомендации по действующим веществам/средствам,
 * будет краткая статья что это такое, что делает с кожей итд».
 *
 * Design: each article has 3 short fields exactly mirroring the
 * user-suggested structure — «Что это», «Что делает с кожей»,
 * «Как применять». Stored as a flat `Record<slug, article>` so we
 * can do O(1) substring lookup via `findArticleSlug()`. Slugs are
 * CASE-INSENSITIVE root forms (3-6 chars) chosen to match the
 * morphologically-varied recommendation strings produced by
 * `RECOMMENDATIONS_MAP` / `generateProductLinks` / `buildRoutine`
 * in `src/server/utils/skinScoring.ts`. Examples:
 *
 *   slug "салицилов" → matches "салициловая кислота" (rec)
 *   slug "салицилов" → also  matches "салициловой кислотой" (routine)
 *
 * Adding a new article is a 5-line entry — no schema migrations,
 * no DB, no API contract changes. The lookup function reports
 * `null` for unmatched strings so the calling UI can silently
 * render them as plain text.
 *
 * Coverage-spanning design: each unique slug is selected so that
 * at least one recommendation string emitted by the analysis
 * pipeline hits it. The fallback `null` path keeps
 * `ingredient_articles.ts` — `skinScoring.ts` recommender strings
 * independent: we don't break the analysis if we miss a slug for
 * a future ingredient.
 */

export interface IngredientArticle {
  /**
   * Display title at top of the modal. Russian language; uses the
   * most familiar user-facing form (e.g. «Салициловая кислота»
   * not «BHA»).
   */
  title: string;
  /**
   * ЧТО ЭТО — short definition / chemical class / origin.
   * 1-2 sentences, max ~30 words.
   */
  whatIs: string;
  /**
   * ЧТО ДЕЛАЕТ С КОЖЕЙ — what benefit / effect the user gets.
   * Bullet-points collapsed into a single string with "; " separators
   * so the modal can render it as 1 paragraph with line-breaks
   * inserted visually.
   */
  whatDoes: string;
  /**
   * КАК ПРИМЕНЯТЬ — practical instructions: frequency, day/night,
   * compatibility, SPF reminders. 1-2 sentences.
   */
  howToApply: string;
}

